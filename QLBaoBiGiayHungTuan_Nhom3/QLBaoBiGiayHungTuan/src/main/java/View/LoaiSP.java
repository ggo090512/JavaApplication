    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        String ml = txtMaLoai.getText();
        String tl = txtTenLoai.getText();
        if(tl.length()==0)
            JOptionPane.showMessageDialog(null, "Vui lòng nhập tên loại sản phẩm", "Thông báo", 1);
        else
            if(tl.length()>30)
                JOptionPane.showMessageDialog(null, "Tên loại sản phẩm tối đa 30 kí tự", "Thông báo", 1);
            else
            {
                try{
                    if(cothem == true)
                        lsp.InsertData(tl);
                    else
                        lsp.EditData(ml,tl);
                    ClearData();
                    ShowData();
                }
                catch(SQLException e){
                    JOptionPane.showMessageDialog(null, "Cập nhật thất bại", "Thông báo", 1);
                }
                setNull();
                setKhoa(true);
                setButton(true);
            }
    }//GEN-LAST:event_btnLuuActionPerformed

