    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        String ten = txtTenLoai.getText();
        try {
            if (ten.length() == 0) {
                JOptionPane.showMessageDialog(null, "Chon 1 loai SP de xoa", "Thong bao", 1);
            } else {
                String ml = txtMaLoai.getText();
                if (JOptionPane.showConfirmDialog(null, "Ban chac chan muon xoa loai " + ten + " nay khong ?", "Thong bao", 2) == 0) {
                    lsp.DeleteData(ml);//Goi ham xoa du lieu theo ma loai
                    ClearData();//Xoa du lieu trong table
                    ShowData();//Do du lieu vao table
                    setNull();//Xoa trang TextField
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Xoa that bai", "Thong bao", 1);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblLoaiSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLoaiSPMouseClicked
        // TODO add your handling code here:
        //Hien thi du lieu len cac textfield khi click chuot vao table
        int row = this.tblLoaiSP.getSelectedRow();
        String ml = (this.tblLoaiSP.getModel().getValueAt(row, 0)).toString();
        try{
            //Lay chi so dong dang chon
            ResultSet rs= lsp.ShowLoaiSP(ml);//Goi ham lay du lieu theo ma
            if(rs.next())//Neu co du lieu
            {
                this.txtMaLoai.setText(rs.getString("MaLoai"));
                this.txtTenLoai.setText(rs.getString("TenLoaiSP"));
            }
        }
        catch(SQLException e){
            
        }
    }//GEN-LAST:event_tblLoaiSPMouseClicked

